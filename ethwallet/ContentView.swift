// MARK: - Ê∂àÊÅØË°å
struct MessageRow: View {
    let message: BLEMessage
    
    var iconName: String {
        switch message.type {
        case .text: return "text.bubble"
        case .binary: return "01.square"
        case .command: return "terminal"
        case .notification: return "bell"
        case .error: return "exclamationmark.triangle"
        }
    }
    
    var iconColor: Color {
        switch message.type {
        case .error: return .red
        case .notification: return .orange
        case .command: return .blue
        default: return message.direction == .sent ? .green : .purple
        }
    }
    
    var body: some View {
        HStack(alignment: .top) {
            Image(systemName: iconName)
                .foregroundColor(iconColor)
                .frame(width: 30)
            
            VStack(alignment: .leading, spacing: 4) {
                Text(message.content)
                    .font(.system(.body, design: .monospaced))
                    .lineLimit(3)
                
                HStack {
                    Text(message.timestamp, format: .dateTime.hour().minute().second())
                        .font(.caption2)
                        .foregroundColor(.secondary)
                    
                    if message.dataSize > 0 {
                        Text("‚Ä¢ \(message.dataSize) bytes")
                            .font(.caption2)
                            .foregroundColor(.secondary)
                    }
                    
                    Text("‚Ä¢ \(message.direction == .sent ? "ÂèëÈÄÅ" : "Êé•Êî∂")")
                        .font(.caption2)
                        .foregroundColor(message.direction == .sent ? .green : .purple)
                }
            }
            
            Spacer()
        }
        .padding(.vertical, 4)
    }
}

